    private void updateFinalTime() {
        double endtimeTemp = endtime;
        try {
        // Make sure bad bparsing doesn't mess up endtime
         endtimeTemp = Double.parseDouble(finalTimeTextField.getText());
        }
        catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Error parsing end time, please retry. End time unchanged.");
        }
        endtime = endtimeTemp;
    }

